// rom_charset.h - the real Oric Atmos ROM's own standard 6x8 character
// generator data (codes 0x20-0x7F, 96 glyphs x 8 bytes), extracted once
// from Phosphoric's roms/basic11b.rom (a real Atmos BASIC 1.1 ROM image,
// 16KB flat binary mapped 1:1 to $C000-$FFFF -- offset = $FC78-$C000 =
// 0x3C78, matching oric.h's own CHARSETROM address) via a one-off Python
// script. Standard machine system data (every Oric program implicitly
// depends on this exact font being present in ROM), not third-party
// creative content -- verified by hand-decoding the 'A' glyph's own bit
// pattern before baking this in.
//
// Needed as a compiled-in copy, not a live read of CHARSETROM ($FC78)
// itself, because that address is NOT reliably valid on either of this
// project's own targets by the time hb_put_chars() wants it: on the
// tape/LOCI target, arkos_load() already banked ROM out via
// enable_overlay_ram() for the rest of the program's runtime (see
// docs/arkos.md) before any section runs; on the floppy target, there is
// NO real ROM at all, ever (see docs/floppy.md, "full RAM mapped at
// $C000-$FFFF for the whole session") -- CHARSETROM was never valid font
// data there in the first place. Copied into HIRES_CHARSET_STD ($9800,
// already-reserved RAM for exactly this kind of HIRES-mode charset need)
// once at boot, right after hires_on() -- see main.c -- and read from
// there by hires.c's hb_put_chars(), not from CHARSETROM directly.

